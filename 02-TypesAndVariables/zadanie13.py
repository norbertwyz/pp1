# Calculation of the area and circumference of a circle

# determine radius and PI
r=5
from math import pi
print(f"The radius of the circle is {r} and the Pi is {pi}.")

# calculate area (pi*r**2)
print(f"The area of the circle is {pi*r**2}.")

# calculate circumference (2*pi*r)
print(f"The circumference of the circle is {2*pi*r}.")

